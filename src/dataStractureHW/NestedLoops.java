package dataStractureHW;

public class NestedLoops {



        public static void main(String[] args) {
            for (int a = 0; a < 1; a++) {

                System.out.println("          *");
                System.out.println("         * *");
                System.out.println("        * * *");
                System.out.println("       * * * *");


                for (int b = 0; b < 1; b++) {
                    System.out.println("      * * * * *");
                    System.out.println("     * * * * * *");
                    System.out.println("    * * * * * * *");
                    System.out.println("   * * * * * * * *");

                    System.out.println("\n" + "See! I technically used nested loop to print a pyramid ");

                }

            }
        }
    }

